import Foundation
import UIKit

// MARK - UIColor extensions
extension UIColor {
    public class var BWStatusGreen: UIColor { get { return UIColor.init(red: 158/255.0, green: 198/255.0, blue: 132/255.0, alpha: 1.0) } }
    public class var BWStatusRed:   UIColor { get { return UIColor.init(red: 227/255.0, green: 198/255.0, blue: 187/255.0, alpha: 1.0) } }
}
